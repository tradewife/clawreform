//! Real HTTP integration tests for the ClawReform API.
//!
//! These tests boot a real kernel, start a real axum HTTP server on a random
//! port, and hit actual endpoints with reqwest.  No mocking.
//!
//! Tests that require an LLM API call are gated behind GROQ_API_KEY.
//!
//! Run: cargo test -p clawreform-api --test api_integration_test -- --nocapture

use axum::Router;
use clawreform_api::middleware;
use clawreform_api::routes::{self, AppState};
use clawreform_api::ws;
use clawreform_kernel::ClawReformKernel;
use clawreform_types::config::{DefaultModelConfig, KernelConfig};
use std::sync::Arc;
use std::time::Instant;
use tower_http::cors::CorsLayer;
use tower_http::trace::TraceLayer;

async fn local_tcp_supported() -> bool {
    tokio::net::TcpListener::bind("127.0.0.1:0").await.is_ok()
}

macro_rules! skip_if_no_local_tcp {
    () => {
        if !local_tcp_supported().await {
            eprintln!("skipping test: local TCP listeners are not permitted in this environment");
            return;
        }
    };
}

